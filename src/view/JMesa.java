    private Jogador getJogadorAtualNaMesa(Mesa mesa) {
        Jogador jogador1 = mesa.getJogador1();
        Jogador jogador2 = mesa.getJogador2();
        
        if (this.atorJogador.getJogadorAtual().getNome().equals(jogador1.getNome())){
            return jogador1;
        } else {
            return jogador2;
        }
    }
    
